export function buildArchiveRetryStatusTemplate(input: {
  success: boolean
  machineName: string
  reference: string
  completedAt: string
  errorMessage?: string
}) {
  const subject = input.success ? 'Archive Delivery Retry - Success' : 'Archive Delivery Retry - Failed'
  const header = input.success
    ? 'Archive delivery retry completed successfully.'
    : 'Archive delivery retry failed.'

  const text = [
    'Hello,',
    '',
    header,
    '',
    `Machine: ${input.machineName}`,
    `Reference Number: ${input.reference}`,
    `Timestamp: ${input.completedAt}`,
    ...(input.errorMessage ? [`Error: ${input.errorMessage}`] : []),
    '',
    'Regards,',
    '',
    'MGMT Inspect',
  ].join('\n')

  return {
    subject,
    text,
    html: [
      '<div style="font-family:Arial,sans-serif;background:#f8fafc;padding:18px;color:#0f172a;">',
      '<div style="max-width:680px;margin:0 auto;background:#ffffff;border:1px solid #e2e8f0;border-radius:12px;overflow:hidden;">',
      '<div style="padding:16px 20px;background:#0f172a;color:#ffffff;">',
      `<h2 style="margin:0;font-size:20px;">${escapeHtml(subject)}</h2>`,
      '</div>',
      `<div style="padding:18px 20px;font-size:14px;line-height:1.6;">${textToHtmlParagraphs(text)}</div>`,
      '</div></div>',
    ].join(''),
  }
}

