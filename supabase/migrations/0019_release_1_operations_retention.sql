-- Archive metadata and immutable PDF payload reference.
create table if not exists public.inspection_archives (
  id uuid primary key default uuid_generate_v4(),
  inspection_id uuid not null unique references public.inspections(id) on delete cascade,
  file_name text not null,
  content_type text not null default 'application/pdf',
  pdf_base64 text not null,
  checksum text,
  generated_at timestamp with time zone not null default now(),
  generated_by uuid references auth.users(id) on delete set null,
  created_at timestamp with time zone not null default now()
);

create index if not exists idx_inspection_archives_generated_at
  on public.inspection_archives(generated_at);

-- Delivery attempt log for archive workflow.
create table if not exists public.archive_delivery_logs (
  id uuid primary key default uuid_generate_v4(),
  inspection_id uuid not null references public.inspections(id) on delete cascade,
  archive_id uuid references public.inspection_archives(id) on delete set null,
  pdf_generated boolean not null default false,
  email_sent boolean not null default false,
  archived boolean not null default false,
  recipient_snapshot jsonb not null default '[]'::jsonb,
  status text not null,
  failure_reason text,
  retry_count integer not null default 0,
  created_at timestamp with time zone not null default now(),
  constraint archive_delivery_logs_status_check check (status in ('success', 'failed', 'retrying', 'skipped'))
);

create index if not exists idx_archive_delivery_logs_inspection_id
  on public.archive_delivery_logs(inspection_id, created_at desc);

create index if not exists idx_archive_delivery_logs_status
  on public.archive_delivery_logs(status, retry_count, created_at);

-- Retention configuration.
create table if not exists public.retention_settings (
  id uuid primary key default uuid_generate_v4(),
  retention_days integer not null default 90,
  use_custom boolean not null default false,
  custom_days integer,
  max_delivery_retries integer not null default 3,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),
  constraint retention_settings_days_check check (retention_days in (30, 60, 90, 180) or use_custom),
  constraint retention_settings_custom_days_check check (custom_days is null or custom_days >= 1),
  constraint retention_settings_retries_check check (max_delivery_retries >= 0)
);

create unique index if not exists idx_retention_settings_singleton
  on public.retention_settings ((true));

insert into public.retention_settings (retention_days, use_custom, custom_days, max_delivery_retries)
select 90, false, null, 3
where not exists (select 1 from public.retention_settings);

create table if not exists public.scheduled_cleanup_runs (
  id uuid primary key default uuid_generate_v4(),
  started_at timestamp with time zone not null default now(),
  completed_at timestamp with time zone,
  status text not null default 'running',
  summary jsonb not null default '{}'::jsonb,
  constraint scheduled_cleanup_runs_status_check check (status in ('running', 'success', 'failed'))
);

create index if not exists idx_scheduled_cleanup_runs_started_at
  on public.scheduled_cleanup_runs(started_at desc);

-- Add archive workflow state directly on inspections for retention guards.
alter table public.inspections
  add column if not exists archive_status text not null default 'pending',
  add column if not exists archived_at timestamp with time zone,
  add column if not exists archived_reference text,
  add column if not exists archive_retry_count integer not null default 0,
  add column if not exists archive_last_error text,
  add column if not exists archive_last_attempt_at timestamp with time zone;

do $$
begin
  if not exists (
    select 1
    from pg_constraint
    where conname = 'inspections_archive_status_check'
      and conrelid = 'public.inspections'::regclass
  ) then
    alter table public.inspections
      add constraint inspections_archive_status_check
      check (archive_status in ('pending', 'archived', 'failed'));
  end if;
end
$$;

create index if not exists idx_inspections_archive_status
  on public.inspections(archive_status, archive_retry_count, completed_at);

create index if not exists idx_inspections_archived_at
  on public.inspections(archived_at);

-- Keep updated_at columns current.
do $$
begin
  if exists (
    select 1 from pg_proc
    where proname = 'update_updated_at_column'
      and pronamespace = 'public'::regnamespace
  ) then
    if not exists (
      select 1 from pg_trigger
      where tgname = 'update_company_settings_updated_at'
        and tgrelid = 'public.company_settings'::regclass
    ) then
      create trigger update_company_settings_updated_at
        before update on public.company_settings
        for each row execute function public.update_updated_at_column();
    end if;

    if not exists (
      select 1 from pg_trigger
      where tgname = 'update_email_distribution_recipients_updated_at'
        and tgrelid = 'public.email_distribution_recipients'::regclass
    ) then
      create trigger update_email_distribution_recipients_updated_at
        before update on public.email_distribution_recipients
        for each row execute function public.update_updated_at_column();
    end if;

    if not exists (
      select 1 from pg_trigger
      where tgname = 'update_email_templates_updated_at'
        and tgrelid = 'public.email_templates'::regclass
    ) then
      create trigger update_email_templates_updated_at
        before update on public.email_templates
        for each row execute function public.update_updated_at_column();
    end if;

    if not exists (
      select 1 from pg_trigger
      where tgname = 'update_retention_settings_updated_at'
        and tgrelid = 'public.retention_settings'::regclass
    ) then
      create trigger update_retention_settings_updated_at
        before update on public.retention_settings
        for each row execute function public.update_updated_at_column();
    end if;
  end if;
end
$$;

