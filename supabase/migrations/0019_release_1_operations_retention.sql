-- Email distribution recipients.
create table if not exists public.email_distribution_recipients (
  id uuid primary key default uuid_generate_v4(),
  name text not null,
  email text not null,
  recipient_type text not null,
  enabled boolean not null default true,
  delivery_scope text not null default 'all_inspections',
  department_filter text,
  machine_filter uuid references public.machines(id) on delete set null,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),
  constraint email_distribution_recipients_type_check check (recipient_type in ('to', 'cc', 'bcc')),
  constraint email_distribution_recipients_scope_check check (
    delivery_scope in ('all_inspections', 'passed_inspections', 'failed_inspections', 'failed_only', 'defects_only')
  )
);

create index if not exists idx_email_distribution_recipients_enabled
  on public.email_distribution_recipients(enabled, recipient_type);

create index if not exists idx_email_distribution_recipients_delivery_scope
  on public.email_distribution_recipients(delivery_scope);

create index if not exists idx_email_distribution_recipients_department
  on public.email_distribution_recipients(department_filter);

create index if not exists idx_email_distribution_recipients_machine
  on public.email_distribution_recipients(machine_filter);

