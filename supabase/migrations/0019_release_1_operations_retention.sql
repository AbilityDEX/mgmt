-- Company settings used by reports and outbound email templates.
create table if not exists public.company_settings (
  id uuid primary key default uuid_generate_v4(),
  company_name text not null default 'MGMT Inspect',
  logo_url text,
  address text,
  telephone text,
  email text,
  website text,
  report_footer text,
  report_primary_color text not null default '#0f766e',
  report_accent_color text not null default '#0f172a',
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),
  constraint company_settings_singleton check (true)
);

create unique index if not exists idx_company_settings_singleton
  on public.company_settings ((true));

