-- Keep alias columns synchronized.
create or replace function public.sync_inspection_archive_compat_columns()
returns trigger as $$
begin
  new.archived_at := coalesce(new.archived_at, new.archive_timestamp);
  new.archive_timestamp := coalesce(new.archive_timestamp, new.archived_at);
  new.archived_reference := coalesce(new.archived_reference, new.archive_reference);
  new.archive_reference := coalesce(new.archive_reference, new.archived_reference);
  return new;
end;
$$ language plpgsql;

do $$
begin
  if not exists (
    select 1 from pg_trigger
    where tgname = 'sync_inspection_archive_compat_columns'
      and tgrelid = 'public.inspections'::regclass
  ) then
    create trigger sync_inspection_archive_compat_columns
      before insert or update on public.inspections
      for each row
      execute function public.sync_inspection_archive_compat_columns();
  end if;
end
$$;

