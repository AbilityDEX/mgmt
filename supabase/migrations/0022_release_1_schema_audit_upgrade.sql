  email,
  recipient_type,
  enabled,
  delivery_scope,
  department_filter,
  machine_filter,
  source_recipient_id
)
select
  r.name,
  r.email,
  r.recipient_type,
  r.enabled,
  r.delivery_scope,
  r.department_filter,
  r.machine_filter,
  r.id
from public.email_distribution_recipients r
where not exists (
  select 1
  from public.email_recipients er
  where er.source_recipient_id = r.id
);

